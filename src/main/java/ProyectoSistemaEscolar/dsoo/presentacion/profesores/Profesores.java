    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try {
            int fila_sel = profeTb.getSelectedRow();
            
            if (fila_sel == -1) {
                throw new IndexOutOfBoundsException("No se ha seleccionado ninguna fila");
            }
            
            Profesor datoModifi = new Profesor();
            datoModifi.setId_profesor((int) dtm.getValueAt(fila_sel, 0));
            datoModifi.setNombres((String) dtm.getValueAt(fila_sel, 1));
            datoModifi.setApellido_paterno((String) dtm.getValueAt(fila_sel, 2));
            datoModifi.setApellido_materno((String) dtm.getValueAt(fila_sel, 3));
            datoModifi.setCorreo((String) dtm.getValueAt(fila_sel, 5));
            datoModifi.setTelefono((String) dtm.getValueAt(fila_sel, 4));

            ProfesoresModal ventaSecundaria = new ProfesoresModal();
            ventaSecundaria.opcion(2, this);
            ventaSecundaria.asignar(datoModifi);
            ventaSecundaria.setVisible(true);
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar primero un profesor de la tabla.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editarActionPerformed

