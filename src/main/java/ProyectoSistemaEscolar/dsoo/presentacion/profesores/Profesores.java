    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        try {
            int fila_sel = profeTb.getSelectedRow();
            ProfesorDaoImpl profeService = new ProfesorDaoImpl();
            int ayuda = (int) dtm.getValueAt(fila_sel, 0);
        
            if(JOptionPane.showConfirmDialog(rootPane, "Seguro que desea eliminarlo?", "Eliminar profesor", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)==0){
                profeService.eliminarProfesor(ayuda);
        
                for (int i = 0; i < profeTb.getRowCount(); i++) {
                    dtm.removeRow(i);
                    i-=1;
                }
                listar();
                JOptionPane.showMessageDialog(rootPane, "Profesor eliminado");
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se elimino");
            }
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar primero un profesor de la tabla.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_eliminarActionPerformed

