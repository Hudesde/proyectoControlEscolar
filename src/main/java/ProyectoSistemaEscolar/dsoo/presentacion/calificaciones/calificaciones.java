    private void enviarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarBtnActionPerformed
            if(idG != -1){
            CalificacionDaoImpl instance = new CalificacionDaoImpl();
            Calificacion datoModifi = new Calificacion();
            
            
            try {
                // Intenta convertir el texto a entero
                int parcial = Integer.parseInt(parcialTxt.getText());
                if(parcial > 10)
                    throw new NumberFormatException("Excedido");
                
                int parcial2 = Integer.parseInt(parcial2Txt.getText());
                if(parcial2 > 10)
                    throw new NumberFormatException("Excedido");
                
                int parcial3 = Integer.parseInt(parcial3Txt.getText());
                if(parcial3 > 10)
                    throw new NumberFormatException("Excedido");
                
                int ordinario = Integer.parseInt(ordinarioTxt.getText());
                if(ordinario > 10)
                    throw new NumberFormatException("Excedido");
                
                int extra = Integer.parseInt(extraTxt.getText());
                if(extra > 10)
                    throw new NumberFormatException("Excedido");
                
                datoModifi.setIdCalificacion(idG);
                datoModifi.setParcial1(parcial);
                datoModifi.setParcial2(parcial2);
                datoModifi.setParcial3(parcial3);
                datoModifi.setOrdinario(ordinario);
                datoModifi.setExtra1(extra);
                
                if(instance.modificarCalificacion(datoModifi))
                    JOptionPane.showMessageDialog(rootPane,"Modificado");
                else
                    JOptionPane.showMessageDialog(rootPane,"No se pudo modificar");
                idG = -1;
                parcialTxt.setText("");
                parcial2Txt.setText("");
                parcial3Txt.setText("");
                ordinarioTxt.setText("");
                extraTxt.setText("");
                llamar();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "No se permiten numeros con decimales o mayores a 10.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane,"Seleccione un alumno para modificar");
        }
    }//GEN-LAST:event_enviarBtnActionPerformed

