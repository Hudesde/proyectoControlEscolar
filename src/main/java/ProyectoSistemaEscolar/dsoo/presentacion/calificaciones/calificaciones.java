    private void listar(){
        AlumnoDaoImpl alumnoService = new AlumnoDaoImpl();
        ArrayList<Alumno> alumnos = alumnoService.listarAlumno();
        for (Alumno alumno : alumnos) {
            //alumnosBox.addItem(alumno.getNombre());
        }
        GrupoDaoImpl instance = new GrupoDaoImpl();
        ArrayList<Grupo> grupos = instance.listarGrupo();
        for (Grupo grupo : grupos) {
            //gruposBox.addItem(grupo.getNombre());
            //System.out.println("id_grupo: "+grupo.getId_grupo()+", nombre: "+grupo.getNombre());
        }
        dtm = (DefaultTableModel) califTb.getModel();
        
        UnionDaoImpl unionService = new UnionDaoImpl();
        CalificacionDaoImpl calif = new CalificacionDaoImpl();
        ArrayList<ArrayList<String>> dobleArrayList = unionService.listarMateriasTodo();
        for (ArrayList<String> lista : dobleArrayList) {
            int i = 0;
            for (String elemento : lista) {
                fila[i] = elemento;
                if(i==4){
                    i++;
                    //System.out.println("entramos");
                    Calificacion ca = calif.listarCalificacionesId(Integer.parseInt(elemento));
                    fila[i] = ca.getParcial1();
                    i++;
                    fila[i] = ca.getParcial2();
                    i++;
                    fila[i] = ca.getParcial3();
                    i++;
                    fila[i] = ca.getOrdinario();
                    i++;
                    fila[i] = ca.getExtra1();
                }
                i++;
            }
            dtm.addRow(fila);
        }
    }
    
        
    public void llamar() {
        for (int i = 0; i < califTb.getRowCount(); i++) {
            dtm.removeRow(i);
            i--;
        }
        listar();
        //listarCombo();
    }
    
