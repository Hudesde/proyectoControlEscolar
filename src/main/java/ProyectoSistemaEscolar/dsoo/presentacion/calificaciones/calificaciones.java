    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try {
            int fila_sel = califTb.getSelectedRow();
            
            if (fila_sel == -1) {
                throw new IndexOutOfBoundsException("No se ha seleccionado ninguna fila");
            }
            //System.out.println( dtm.getValueAt(fila_sel, 4));
            idG = Integer.parseInt(dtm.getValueAt(fila_sel, 4).toString());
            parcialTxt.setText(dtm.getValueAt(fila_sel, 5).toString());
            parcial2Txt.setText(dtm.getValueAt(fila_sel, 6).toString());
            parcial3Txt.setText(dtm.getValueAt(fila_sel, 7).toString());
            ordinarioTxt.setText(dtm.getValueAt(fila_sel, 8).toString());
            extraTxt.setText(dtm.getValueAt(fila_sel, 9).toString());

        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar primero un profesor de la tabla.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editarActionPerformed

