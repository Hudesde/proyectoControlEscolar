    private void listar(){
        dtm = (DefaultTableModel) calificacionesTb.getModel();
        UnionDaoImpl unionService = new UnionDaoImpl();
        CalificacionDaoImpl calif = new CalificacionDaoImpl();
        ProfesorDaoImpl profeService = new ProfesorDaoImpl();
        
        
        ArrayList<Union> Lista = unionService.listarUnionesIdAlumno(login.user_id);
        if(Lista.isEmpty()){
            mensajeTxt.setText("No cuenta con Materias Registradas");
        }else{
            int i = 0;
            for (Union lista : Lista) {
                System.out.println("Entramos");
                System.out.println(lista.getId());
                fila[0] = profeService.listarProfesorMateriaId(lista.getId_profesor());
                Calificacion temp = calif.listarCalificacionesId(lista.getId_calificacion());

                fila[1] = temp.getParcial1();
                fila[2] = temp.getParcial2();
                fila[3] = temp.getParcial3();
                fila[4] = temp.getOrdinario();
                fila[5] = temp.getExtra1();
                dtm.addRow(fila);
            }
        }
    }
    
    public void llamar() {
        for (int i = 0; i < calificacionesTb.getRowCount(); i++) {
            dtm.removeRow(i);
            i--;
        }
        listar();
        //listarCombo();
    }
    
